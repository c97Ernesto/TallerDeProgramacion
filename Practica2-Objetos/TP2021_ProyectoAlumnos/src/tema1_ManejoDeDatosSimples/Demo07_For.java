package tema1_ManejoDeDatosSimples;


public class Demo07_For {

    public static void main(String[] args) {
         System.out.println("Primer for");
         int i;
         for(i=1; i<=10; i++){
              System.out.println("I vale: " + i);
         }
         System.out.println("Segundo for");
         for(i=10; i>0; i=i-1){
              System.out.println("I vale: " + i);
         }

    }
    
}
